/* eigenengine.js — the eigenengine page's behaviour and its two drawn exhibits.
   Everything numeric or drawn comes from window.EE (assets/js/eigenengine-page-data.js,
   generated by scripts/site_assets/build_eigenengine_page_data.py in the engine tree) —
   the page never states a count the generator did not emit.
   Exhibits:
     (1) the conceptric render — baked coordinates, no physics; hover/tap reads a node;
         def-orphans ring red (the reserved refusal colour).
     (2) the convergence gate matrix — a real table of detector verdicts per round.
     (3) the lineage band — paper sections weighted by realised manuscript, the
         unresolved objects as their own band.
   Node-kind hues are the identity's graph-mode family, validated as a categorical set
   against the dark surface (dataviz six-checks): def/claim/limitation/result get hue;
   section/cluster/domain are structural and get neutral + shape instead. */
(function () {
  "use strict";
  var EE = window.EE;
  if (!EE) return;

  var KIND_HUES = { def: "#16A085", claim: "#4A90D9", limitation: "#D35400", result: "#9B5DE5" };
  var NEUTRAL = "#6b7280";
  var REFUSED = "#c0392b";

  /* ── fill every data-ee slot from EE ── */
  var H = EE.hourglass, FC = H.artefact.findings_counts || {};
  var SLOTS = {
    "g-nodes": H.conceptric.nodes, "g-nodes2": H.conceptric.nodes, "g-nodes3": H.conceptric.nodes,
    "g-edges": H.conceptric.edges, "g-edges2": H.conceptric.edges, "g-edges3": H.conceptric.edges,
    "g-kinds": Object.keys(EE.conceptric.kinds).length,
    "sb-sections": H.storyboard.sections, "sb-sections2": H.storyboard.sections,
    "sb-sections3": H.storyboard.sections,
    "sb-appendices": H.storyboard.appendices, "sb-appendices2": H.storyboard.appendices,
    "sb-appendices3": H.storyboard.appendices,
    "cv-rounds": H.converge.rounds, "cv-rounds2": H.converge.rounds,
    "cv-open": H.converge.open_findings,
    "ar-version": H.artefact.version, "ar-version2": H.artefact.version,
    "ar-version3": H.artefact.version,
    "ar-findings": H.artefact.findings, "ar-findings2": H.artefact.findings,
    "ar-hard": FC.hard, "ar-soft": FC.soft, "ar-deferred": FC.deferred,
    "ar-versions": H.artefact.versions,
    "meta-commit": EE.meta.bake_commit, "meta-commit2": EE.meta.bake_commit,
    "meta-commit3": EE.meta.bake_commit,
    "meta-date": EE.meta.bake_date, "meta-date2": EE.meta.bake_date
  };
  document.querySelectorAll("[data-ee]").forEach(function (el) {
    var v = SLOTS[el.getAttribute("data-ee")];
    if (v !== undefined && v !== null) el.textContent = String(v);
  });

  /* ── the band: explode one fold in place; hash deep-links ── */
  var stages = Array.prototype.slice.call(document.querySelectorAll(".ee-stage"));
  var drawn = {};
  function openFold(name, scroll) {
    stages.forEach(function (b) {
      var mine = b.getAttribute("data-fold") === name;
      b.setAttribute("aria-expanded", mine ? "true" : "false");
    });
    ["corpus", "conceptric", "storyboard", "converge", "artefact"].forEach(function (f) {
      var el = document.getElementById("fold-" + f);
      if (el) el.hidden = (f !== name);
    });
    if (name && !drawn[name]) {
      drawn[name] = true;
      if (name === "conceptric") drawGraph();
      if (name === "converge") drawGates();
      if (name === "artefact") drawLineage();
    }
    if (name && scroll) {
      var el2 = document.getElementById("fold-" + name);
      if (el2) el2.scrollIntoView({ behavior: "smooth", block: "nearest" });
    }
  }
  stages.forEach(function (b) {
    b.addEventListener("click", function () {
      var name = b.getAttribute("data-fold");
      var already = b.getAttribute("aria-expanded") === "true";
      openFold(already ? null : name, !already);
      if (history.replaceState) history.replaceState(null, "", already ? "#walk" : "#" + name);
    });
  });
  var initial = (location.hash || "").slice(1);
  if (["corpus", "conceptric", "storyboard", "converge", "artefact"].indexOf(initial) >= 0) {
    openFold(initial, true);
  } else {
    openFold("conceptric", false);   // the graph is the default open stage
  }

  /* ── exhibit 1: the conceptric ── */
  function drawGraph() {
    var canvas = document.getElementById("ee-graph");
    if (!canvas) return;
    var readout = document.getElementById("ee-graph-readout");
    var nodes = EE.conceptric.nodes, edges = EE.conceptric.edges;
    var orphans = {};
    (EE.conceptric.def_orphans || []).forEach(function (id) { orphans[id] = true; });
    var byId = {};
    nodes.forEach(function (n) { byId[n.id] = n; });
    var deg = {};
    edges.forEach(function (e) { deg[e.src] = (deg[e.src] || 0) + 1; deg[e.dst] = (deg[e.dst] || 0) + 1; });

    var W = 1000, Hc = 620;
    function draw(focus) {
      var rect = canvas.parentElement.getBoundingClientRect();
      var dpr = window.devicePixelRatio || 1;
      canvas.width = rect.width * dpr;
      canvas.height = canvas.clientHeight * dpr;
      var ctx = canvas.getContext("2d");
      ctx.scale(dpr, dpr);
      var sx = rect.width / W, sy = canvas.clientHeight / Hc;
      ctx.clearRect(0, 0, rect.width, canvas.clientHeight);
      ctx.lineWidth = 1;
      edges.forEach(function (e) {
        var a = byId[e.src], b = byId[e.dst];
        if (!a || !b) return;
        var hot = focus && (e.src === focus.id || e.dst === focus.id);
        ctx.strokeStyle = hot ? "rgba(201,169,110,0.75)" : "rgba(255,255,255,0.10)";
        ctx.beginPath();
        ctx.moveTo(a.x * sx, a.y * sy);
        ctx.lineTo(b.x * sx, b.y * sy);
        ctx.stroke();
      });
      nodes.forEach(function (n) {
        var x = n.x * sx, y = n.y * sy;
        var r = Math.min(11, 3.4 + (deg[n.id] || 0) * 0.55);
        var structural = !(n.kind in KIND_HUES);
        ctx.beginPath();
        if (n.kind === "section") {
          ctx.rect(x - r * 0.85, y - r * 0.85, r * 1.7, r * 1.7);
        } else {
          ctx.arc(x, y, r, 0, Math.PI * 2);
        }
        ctx.fillStyle = structural ? NEUTRAL : KIND_HUES[n.kind];
        ctx.globalAlpha = focus && focus.id !== n.id ? 0.55 : 1;
        ctx.fill();
        ctx.globalAlpha = 1;
        if (n.kind === "cluster" || n.kind === "domain") {
          ctx.strokeStyle = "rgba(255,255,255,0.5)";
          ctx.lineWidth = 1.4;
          ctx.stroke();
          ctx.lineWidth = 1;
        }
        if (orphans[n.id]) {
          ctx.beginPath();
          ctx.arc(x, y, r + 2.6, 0, Math.PI * 2);
          ctx.strokeStyle = REFUSED;
          ctx.lineWidth = 1.8;
          ctx.stroke();
          ctx.lineWidth = 1;
        }
        if (focus && focus.id === n.id) {
          ctx.beginPath();
          ctx.arc(x, y, r + 4, 0, Math.PI * 2);
          ctx.strokeStyle = "#dfc599";
          ctx.lineWidth = 1.6;
          ctx.stroke();
          ctx.lineWidth = 1;
        }
      });
    }

    function nearest(mx, my) {
      var rect = canvas.getBoundingClientRect();
      var sx = rect.width / W, sy = rect.height / Hc;
      var best = null, bd = 18 * 18;
      nodes.forEach(function (n) {
        var dx = n.x * sx - mx, dy = n.y * sy - my;
        var d = dx * dx + dy * dy;
        if (d < bd) { bd = d; best = n; }
      });
      return best;
    }
    function show(n) {
      draw(n);
      if (!readout) return;
      if (!n) {
        readout.innerHTML = "<span class='ee-dim'>hover a node — tap on a phone</span>";
        return;
      }
      var e = deg[n.id] || 0;
      readout.innerHTML = "<b>" + esc(n.label) + "</b><br>" +
        esc(n.kind) + " · " + e + " edge" + (e === 1 ? "" : "s") +
        (orphans[n.id] ? " · <span style='color:" + REFUSED + "'>def-orphan (no claim consumes it)</span>" : "") +
        " · <span class='ee-dim'>" + esc(n.id) + "</span>";
    }
    canvas.addEventListener("mousemove", function (ev) {
      var r = canvas.getBoundingClientRect();
      show(nearest(ev.clientX - r.left, ev.clientY - r.top));
    });
    canvas.addEventListener("mouseleave", function () { show(null); });
    canvas.addEventListener("click", function (ev) {
      var r = canvas.getBoundingClientRect();
      show(nearest(ev.clientX - r.left, ev.clientY - r.top));
    });
    window.addEventListener("resize", function () { draw(null); });
    draw(null);

    var legend = document.getElementById("ee-legend");
    if (legend) {
      var k = EE.conceptric.kinds;
      var rows = [];
      Object.keys(KIND_HUES).forEach(function (kind) {
        if (k[kind]) rows.push("<span class='lg'><span class='dot' style='background:" +
          KIND_HUES[kind] + "'></span>" + kind + " <span class='num'>" + k[kind] + "</span></span>");
      });
      if (k.section) rows.push("<span class='lg'><span class='sq' style='background:" + NEUTRAL +
        "'></span>section <span class='num'>" + k.section + "</span></span>");
      var structN = (k.cluster || 0) + (k.domain || 0);
      if (structN) rows.push("<span class='lg'><span class='dot' style='background:" + NEUTRAL +
        ";box-shadow:0 0 0 1.5px rgba(255,255,255,0.5)'></span>cluster/root <span class='num'>" +
        structN + "</span></span>");
      rows.push("<span class='lg'><span class='rg'></span>def-orphan <span class='num'>" +
        (EE.conceptric.def_orphans || []).length + "</span></span>");
      legend.innerHTML = rows.join("");
    }
  }

  /* ── exhibit 2: the gate matrix ── */
  function drawGates() {
    var host = document.getElementById("ee-gates");
    if (!host) return;
    var G = EE.gates;
    var head = "<tr><th style='text-align:left'>gate</th>" + G.rounds.map(function (r) {
      return "<th>" + esc(r.label) + "</th>";
    }).join("") + "</tr>";
    var body = G.gates.map(function (g) {
      return "<tr><td class='g-name'>" + esc(g) + "</td>" + G.rounds.map(function (r) {
        var ok = r.clean[g];
        return "<td class='" + (ok ? "g-clean" : "g-fail") + "' title='" + esc(g) +
          " — " + (ok ? "clean" : "failing") + " at " + esc(r.label) + "'>" +
          (ok ? "&#10003;" : "&#10007;") + "</td>";
      }).join("") + "</tr>";
    }).join("");
    var findings = "<tr class='g-findings'><td class='g-name'>open findings</td>" +
      G.rounds.map(function (r) { return "<td>" + r.open_findings + "</td>"; }).join("") + "</tr>";
    host.innerHTML = "<div class='ee-gates-scroll'><table class='ee-gates-table'>" +
      "<caption class='ee-addr' style='caption-side:top;text-align:left;padding:0 0 0.4rem'>" +
      "detector verdicts per convergence round — paper_falqon, rounds/harnessed/detect_*.json, " +
      "baked at " + esc(EE.meta.bake_commit) + " · &#10003; clean · &#10007; failing</caption>" +
      head + body + findings + "</table></div>";
  }

  /* ── exhibit 3: the lineage band ── */
  function drawLineage() {
    var host = document.getElementById("ee-lineage");
    if (!host) return;
    var whole = EE.lineage.whole || {};
    var regions = (whole.regions || []).filter(function (r) { return r.weight > 0; });
    var unres = ((whole.unresolved || {}).groups || []);
    var unresN = unres.reduce(function (s, g) { return s + (g.n || 0); }, 0);
    var total = regions.reduce(function (s, r) { return s + r.weight; }, 0) + unresN;
    if (!total) return;

    var tip = document.createElement("div");
    tip.className = "ee-lineage-tip";
    document.body.appendChild(tip);

    var maxW = Math.max.apply(null, regions.map(function (r) { return r.weight; }));
    var band = "<div class='ee-lineage-band' role='img' aria-label='Manuscript weight per paper section, with unresolved objects as the final band'>";
    regions.forEach(function (r) {
      var frac = r.weight / total;
      var shade = 0.35 + 0.65 * (r.weight / maxW);
      band += "<span class='seg' style='flex:" + Math.max(frac, 0.012) +
        ";background:rgba(201,169,110," + shade.toFixed(2) + ")' data-tip='" +
        esc(r.title) + " — " + r.realised + " concept" + (r.realised === 1 ? "" : "s") +
        " realised, " + r.objects + " object" + (r.objects === 1 ? "" : "s") + "'></span>";
    });
    band += "<span class='seg unres' style='flex:" + Math.max(unresN / total, 0.012) +
      ";background:rgba(192,57,43,0.55)' data-tip='unresolved — " +
      unres.map(function (g) { return g.n + " " + esc(g.kind) + "s"; }).join(", ") +
      "'></span></div>";

    var top = regions.slice().sort(function (a, b) { return b.weight - a.weight; }).slice(0, 3);
    var labels = "<div class='ee-lineage-labels'>" + top.map(function (r) {
      return "<span class='lab'><b>" + esc(r.title) + "</b> " +
        Math.round(100 * r.weight / total) + "%</span>";
    }).join("") + "<span class='lab' style='color:" + REFUSED + "'>unresolved " +
      Math.round(100 * unresN / total) + "%</span></div>";

    host.innerHTML = band + labels +
      "<span class='ee-addr'>section weight = how much of the manuscript realises in it · " +
      "lineage index, baked at " + esc(EE.meta.bake_commit) + " · hover a band for its section</span>";

    host.querySelectorAll(".seg").forEach(function (seg) {
      seg.addEventListener("mousemove", function (ev) {
        tip.textContent = seg.getAttribute("data-tip");
        tip.style.display = "block";
        tip.style.left = Math.min(ev.clientX + 12, window.innerWidth - 260) + "px";
        tip.style.top = (ev.clientY + 14) + "px";
      });
      seg.addEventListener("mouseleave", function () { tip.style.display = "none"; });
    });

    var tbl = document.getElementById("ee-lineage-table");
    if (tbl) {
      tbl.innerHTML = "<table><tr><th>section</th><th>weight</th><th>concepts realised</th>" +
        "<th>objects</th></tr>" + regions.map(function (r) {
          return "<tr><td>" + esc(r.title) + "</td><td>" + r.weight + "</td><td>" +
            r.realised + "</td><td>" + r.objects + "</td></tr>";
        }).join("") + "<tr><td style='color:" + REFUSED + "'>unresolved</td><td>" + unresN +
        "</td><td>—</td><td>" + unresN + "</td></tr></table>";
    }
  }

  function esc(s) {
    return String(s == null ? "" : s).replace(/[&<>"']/g, function (c) {
      return { "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" }[c];
    });
  }
})();
